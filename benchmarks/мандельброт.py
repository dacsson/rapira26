#!/usr/bin/env python3

def создать_заголовок(ширина, высота):
    print("P4")
    print(f"{ширина} {высота}")


def сопоставить_пиксель_х(н, х):
    хмин = -1.5
    хмакс = 0.5
    return хмин + (х / (н - 1)) * (хмакс - хмин)


def сопоставить_пиксель_у(н, у):
    умин = -1.0
    умакс = 1.0
    return умин + (у / (н - 1)) * (умакс - умин)


def итерирование(рац, ирац, макс_итер):
    зрац = 0.0
    зирац = 0.0

    for и in range(макс_итер + 1):
        темп = зрац * зрац - зирац * зирац + рац
        зирац = 2.0 * зрац * зирац + ирац
        зрац = темп

        if зрац * зрац + зирац * зирац > 4.0:
            return и

    return макс_итер


def прочитать_целое():
    while True:
        try:
            return int(input())
        except ValueError:
            print("Ошибка: ожидается целое число")


def вход():
    н = прочитать_целое()

    создать_заголовок(н, н)

    шейдеры = [" ", "░", "▒", "▓", "█"]

    for иу in range(н + 1):
        линия = ""
        рац = сопоставить_пиксель_у(н, иу)
        for их in range(н + 1):
            ирац = сопоставить_пиксель_х(н, их)
            итер = итерирование(ирац, рац, 1000)
            индекс_шейдера = итер * (len(шейдеры) - 1) // 1000
            линия += шейдеры[индекс_шейдера]
        print(линия)


if __name__ == "__main__":
    вход()
